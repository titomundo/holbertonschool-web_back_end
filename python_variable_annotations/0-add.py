#!/usr/bin/env python3

"""0-add.py
Make a function that adds two floats and returns the sum as a float
"""


def add(a: float, b: float) -> float:
    """Sums two floats

    Args:
        a (float): first float
        b (float): second float

    Returns:
    float: The sum of a and b
    """
    return a + b
