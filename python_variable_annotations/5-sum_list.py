#!/usr/bin/env python3

"""5-sum_list:
Get the sum of a list of floats
"""


def sum_list(input_list: list[float]) -> float:
    """
    Returns the sum of a list of floats

    Args:
    input_list (list[float]): list of floats

    Returns:
    float: Sum of floats
    """
    return sum(input_list)
