#!/usr/bin/env python3

"""1-concat:
Concatenates two string arguments and return the new string
"""


def concat(str1: str, str2: str) -> str:
    """
    Concatenates two strings

    Args:
    str1 (str): First string
    str2 (str): Second string

    Returns:
    str: New string created by joining str1 and str2
    """
    return str1 + str2
